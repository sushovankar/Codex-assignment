
public class CB1A1Q3 {

	public static void main(String[] args) {
    int a=7,b=9; 
    System.out.println("a."+(a&b));   //bitwise AND
    System.out.println("b."+(a|b));   //bitwise OR
    System.out.println("c."+(a^b));   //bitwise XOR
    System.out.println("d."+(a<<2));  //left shift
    System.out.println("d."+(a>>2));  //right shift
    
    
    
	}

}

